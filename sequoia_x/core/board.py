"""板块涨跌停幅度判定：按 A 股代码前缀返回当日涨跌停幅度。

A股不同板块涨跌停限制不同，固定 10% 阈值会误判创业板/科创板/北交所。
涨停/跌停类策略据此自适配，避免假阳性。
"""

# 判定"贴近涨跌停"的容差，替代旧的魔法数（1.095 / 0.905）。
# 例：主板 (1 + 0.10) * (1 - 0.005) = 1.0945 ≈ 旧的 1.095，主板行为基本不变。
LIMIT_EPS = 0.005


def limit_pct(symbol: str) -> float:
    """返回该股票当日涨跌停幅度（绝对值，按板块代码前缀）。

    Args:
        symbol: 纯数字代码，如 '600000'、'300750'、'688981'、'830879'。

    Returns:
        涨跌停幅度：主板 0.10 / 创业板·科创板 0.20 / 北交所 0.30。
    """
    if symbol.startswith(("300", "301")):      # 创业板
        return 0.20
    if symbol.startswith("688"):               # 科创板
        return 0.20
    if symbol.startswith(("4", "8", "920")):   # 北交所
        return 0.30
    return 0.10                                 # 沪深主板
